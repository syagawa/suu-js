const makeFibonacciSequence = function(a, b){

  if(!isSu(a) || !isSu(b)){
    return false;
  }

  const MAX = CONSTANTS.MAX;

  const arr = [a, b];
  const func = function(arr){
    if(arr[arr.length - 1].isLarge(MAX)){
      return arr;
    }
    const a = arr[arr.length - 2];
    const b = arr[arr.length - 1];
    const c = a.add(b);
    arr.push(c);
    return func(arr);
  };
  return func(arr);
};


